package com.siddharth.it;

public class ReverceString {
	public static void main (String []args) {
		
		String ch ="rohit";
		String chh="";
		char chi;
	for(int i=0;i<ch.length();i++) {
		
		chi = ch.charAt(i);
		chh=chi+chh;
	}
		
		
		System.out.println(chh);
		
		
		
	}
	


}
